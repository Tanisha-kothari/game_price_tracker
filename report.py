def _dashboard_section(games: list[dict], history: dict) -> str:
    total = len(games)
    on_sale_count = 0
    changed_count = 0
    new_low_count = 0
    cheapest = None
    cheapest_price = None
    most_expensive = None
    most_expensive_price = None

    for g in games:
        cid = g["id"]
        current = g.get("current_price")
        curr_currency = g.get("current_currency", "INR")

        if current is not None:
            if cheapest_price is None or current < cheapest_price:
                cheapest_price = current
                cheapest = g
            if most_expensive_price is None or current > most_expensive_price:
                most_expensive_price = current
                most_expensive = g

        diff = detect_price_change(history, cid, current, curr_currency)
        if diff is not None and diff != 0:
            changed_count += 1
            if diff < 0:
                on_sale_count += 1

        hist_prices = get_history_prices(history, cid)
        if current is not None and len(hist_prices) >= 2:
            prev_lowest = min(hist_prices[:-1])
            if current < prev_lowest:
                new_low_count += 1

    free_count = sum(1 for g in games if g.get("current_price") is not None and g["current_price"] == 0)

    cards = [
        ("🎮", "Tracked", str(total), ""),
        ("🏷️", "On Sale", str(on_sale_count), "#22c55e"),
        ("📉", "Price Changed", str(changed_count), "#f59e0b"),
        ("🏆", "New Lows", str(new_low_count), "#8b5cf6"),
    ]
    cards_html = ""
    for icon, label, value, color in cards:
        val_color = color or "#f1f5f9"
        cards_html += f"""<td style="width:25%;padding:6px;text-align:center;">
<table cellpadding="10" cellspacing="0" style="width:100%;background:rgba(15,23,42,0.6);border-radius:12px;">
<tr><td style="text-align:center;">
<div style="font-size:20px;margin-bottom:2px;">{icon}</div>
<div style="color:{val_color};font-size:22px;font-weight:800;">{value}</div>
<div style="color:#64748b;font-size:11px;text-transform:uppercase;letter-spacing:0.3px;">{label}</div>
</td></tr></table></td>"""

    extras = ""
    if cheapest and cheapest_price is not None:
        extras += f'<p style="color:#94a3b8;font-size:13px;margin:8px 0 0;">Cheapest: <strong style="color:#22c55e;">{cheapest.get("name","?")}</strong> — {format_price(cheapest_price, cheapest.get("current_currency","INR"))}</p>'
    if most_expensive and most_expensive_price is not None:
        extras += f'<p style="color:#94a3b8;font-size:13px;margin:4px 0 0;">Most expensive: <strong style="color:#ef4444;">{most_expensive.get("name","?")}</strong> — {format_price(most_expensive_price, most_expensive.get("current_currency","INR"))}</p>'
