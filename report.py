def _insights_section(games: list[dict], history: dict) -> str:
    price_drops = []
    price_hikes = []
    store_discounts: dict[str, int] = {}
    total_current = 0.0
    count_with_price = 0
    free_count = 0

    for g in games:
        cid = g["id"]
        current = g.get("current_price")
        currency = g.get("current_currency", "INR")
        store = g.get("store", "")
        diff = detect_price_change(history, cid, current, currency)

        if diff is not None:
            if diff < 0:
                price_drops.append((diff, g.get("name", "?"), format_price(abs(diff), currency)))
                store_discounts[store] = store_discounts.get(store, 0) + 1
            elif diff > 0:
                price_hikes.append((diff, g.get("name", "?"), format_price(diff, currency)))

        if current is not None:
            total_current += current
            count_with_price += 1
            if current == 0:
                free_count += 1

    avg_price = total_current / count_with_price if count_with_price > 0 else None
    insights = []
    if price_drops:
        price_drops.sort(key=lambda x: x[0])
        biggest = price_drops[0]
        insights.append(f'Biggest drop: <strong style="color:#22c55e;">▼ {biggest[2]}</strong> — {biggest[1]}')
    else:
        insights.append("No price drops today.")

    if price_hikes:
        price_hikes.sort(key=lambda x: x[0], reverse=True)
        biggest = price_hikes[0]
        insights.append(f'Biggest increase: <strong style="color:#ef4444;">▲ {biggest[2]}</strong> — {biggest[1]}')
    else:
        insights.append("No price increases today.")

    if store_discounts:
        best_store = max(store_discounts, key=store_discounts.get)
        best_count = store_discounts[best_store]
        insights.append(f'Store with most discounts: <strong>{best_store.upper()}</strong> ({best_count})')
    else:
        insights.append("No store has discounts today.")

    if avg_price is not None:
        insights.append(f"Average tracked price: <strong>{format_price(avg_price, 'INR')}</strong>")
    if free_count > 0:
        insights.append(f"Free games: <strong style='color:#22c55e;'>{free_count}</strong>")

    items = "".join(f'<li style="color:#94a3b8;font-size:13px;margin-bottom:6px;line-height:1.4;">{i}</li>' for i in insights)
    return f"""<table width="100%" cellpadding="0" cellspacing="0" style="background:rgba(15,23,42,0.4);border-radius:12px;padding:4px 12px;">
<tr><td><ul style="list-style:none;padding:0;margin:8px 0;">{items}</ul></td></tr>
</table>"""


