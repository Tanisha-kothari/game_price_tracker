def _store_badge(store: str) -> str:
    return STORE_BADGES.get(store, store.upper())


def _price_cell(price: Optional[float], currency: str) -> str:
    display = format_price(price, currency)
    color = "#f1f5f9"
    if display == "N/A":
        color = "#64748b"
    return f'<span style="color:{color};font-size:14px;font-weight:600;">{display}</span>'


def _target_status(game: dict) -> str:
    target = game.get("target_price")
    current = game.get("current_price")
    currency = game.get("current_currency", "INR")
    target_currency = get_price_currency(game, "target_price")
    reached = is_target_met(current, currency, target, target_currency)
    if reached and target is not None:
        return '<span style="color:#22c55e;font-weight:700;">✅ At or Below Target</span>'
    if target is not None and current is not None and target_currency == currency and target > 0:
        diff = abs(current - target)
        above = current > target
        direction = "above" if above else "below"
        color = "#ef4444" if above else "#f59e0b"
        return f'<span style="color:{color};font-size:13px;font-weight:600;">₹{diff:,.0f} {direction} target</span>'
    if target is None:
        return '<span style="color:#64748b;font-size:12px;">No target set</span>'
    return '<span style="color:#64748b;font-size:12px;">N/A</span>'


